#include <iostream>

using namespace std;

int main()
{
    int a,b;
    cin >> a >> b;
    cout << a << " x " << b << " = " << a*b << endl;
    cout << a << " div " << b << " = " << a/b << endl;
    cout << a << " mod " << b << " = " << a%b << endl;
    cout << a << " + " << b << " = " << a+b << endl;
    cout << a << " - " << b << " = " << a-b << endl;
    return 0;
}
